#include <machine/asm.h>

__FBSDID("$FreeBSD$");

LEAF(htons)
#ifdef __MIPSEB__
        move    v0, a0
#else
	srl	v0, a0, 8
	and	v0, v0, 0xff
	sll	v1, a0, 8
	and	v1, v1, 0xff00
	or	v0, v0, v1
#endif
#ifndef __CHERI_PURE_CAPABILITY__
	j	ra
#else
	cjr     $c17
#endif
END(htons)
